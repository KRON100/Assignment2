package question6;

public class pattern {

	public static void main(String[] args) {
	
		int row = 10;
		for (int i = 1; i <= row; i++)  
		{  
			int num = 1;
		for (int j = 1; j <= i; j++)  
		{  
		System.out.print(num+" ");
		num++;
		}  
		   
		System.out.println();  
	}

}
}
